package org.spicefactory.parsley.core.context.provider;

import javax.inject.Provider;

public class ObjectProvider implements Provider<Object> {

	private final Object object;

	public ObjectProvider(Object object) {
		this.object = object;
	}

	/////////////////////////////////////////////////////////////////////////////
	// Package-private.
	/////////////////////////////////////////////////////////////////////////////

	/////////////////////////////////////////////////////////////////////////////
	// Public API.
	/////////////////////////////////////////////////////////////////////////////

	@Override
	public Object get() {
		return object;
	}

	/////////////////////////////////////////////////////////////////////////////
	// Internal implementation.
	/////////////////////////////////////////////////////////////////////////////
}
